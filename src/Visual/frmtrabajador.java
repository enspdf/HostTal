    //al siguiente combobox segun sea el indice.
    public String[] getDepartamento(String puesto){
        String[] departamento = new String [3];
        if (puesto.equalsIgnoreCase("Recepcion")) {
            departamento[0] = "Portero";
            departamento[1] = "Botones";
            departamento[2] = "Recepcionista";
        }
        
        if (puesto.equalsIgnoreCase("Cocina")) {
            departamento[0] = "Chef";
            departamento[1] = "Mesero";
            departamento[2] = "Auxiliar";
        }
        return departamento;
    }
    
