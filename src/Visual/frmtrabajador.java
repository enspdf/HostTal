    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if(txtnombre.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre para el trabajador");
            txtnombre.requestFocus();
            return;
        }
        if(txtprimer_apellido.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un apellido para el trabajador");
            txtprimer_apellido.requestFocus();
            return;
        }
        if(txtsegundo_apellido.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un segundo apellido para el trabajador");
            txtsegundo_apellido.requestFocus();
            return;
        }
        if(txtnum_documento.getText().length()==0){
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un Numero de Doc para el trabajador");
            txtnum_documento.requestFocus();
            return;
        }
        if(txtsueldo.getText().length()==0){
            JOptionPane.showConfirmDialog(this, "Debes ingresar un sueldo para el trabajador");
            txtsueldo.requestFocus();
            return;
        }
        
        vtrabajador dts = new vtrabajador();
        ftrabajador func = new ftrabajador();
        
        dts.setNombre(txtnombre.getText());
        dts.setPrimer_apellido(txtprimer_apellido.getText());
        dts.setSegundo_apellido(txtsegundo_apellido.getText());
        
        int seleccionado = cbotipo_documento.getSelectedIndex();
        dts.setTipo_documento((String)cbotipo_documento.getItemAt(seleccionado));
        dts.setNum_documento(txtnum_documento.getText());
        seleccionado=cbogenero.getSelectedIndex();        
        dts.setGenero((String) cbogenero.getItemAt(seleccionado));
        dts.setDireccion((txtdireccion.getText()));
        dts.setTelefono((txttelefono.getText()));
        dts.setEmail((txtemail.getText()));
        dts.setSueldo(Double.parseDouble(txtsueldo.getText()));
        seleccionado=cbodepartamento.getSelectedIndex();
        dts.setDepartamento((String) cbodepartamento.getItemAt(seleccionado));
        seleccionado=cbopuesto.getSelectedIndex();
        dts.setPuesto((String) cbopuesto.getItemAt(seleccionado));
        seleccionado=cboestado.getSelectedIndex();
        dts.setEstado((String) cboestado.getItemAt(seleccionado));
        
        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(this, "El trabajador fue registrado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
        else if(accion.equals("editar")){
            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(this, "El trabajador fue modificado satisfactoriamente");
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

